/**
 *  加法
 * @param {*} a 
 * @param {*} b 
 */
export function add (a, b) {
  return a + b;
}

/**
 * 减法
 * @param {*} a 
 * @param {*} b 
 */
export function minus (a, b) {
  return a - b;
}

/**
 * 乘法
 * @param {*} a 
 * @param {*} b 
 */
export function multiply (a, b) {
  return a * b;
}

/**
 * 除法
 * @param {*} a 
 * @param {*} b 
 */
export function division (a, b) {
  return a / b;
}